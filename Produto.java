class Produto{
	private String nome;
	private double valor;
	private double peso;
	
	public double getPeso(){
		return peso;
	}
	
	public double getValor(){
		return valor;
	}
	
	public String getNome(){
		return nome;
	}
	
	public void setPeso (double i){
		peso=i;
	}
	
	public void setValor (double i){
		valor=i;
	}
	
	public void setNome (String s){
		nome=s;
	}
	
	
}
